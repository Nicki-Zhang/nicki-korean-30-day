import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawnSync } from 'node:child_process';

const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const workflow=fs.readFileSync(path.join(root,'.github/workflows/generate-lesson-audio.yml'),'utf8');
const config=JSON.parse(fs.readFileSync(path.join(root,'audio-batches.json'),'utf8'));
const batch=config.batches['audio-batch-01'];
assert.equal(Object.keys(config.batches).length,1,'Only Batch 1 may be configured in this workflow.');
assert.equal(batch.expectedCount,2);assert.deepEqual(batch.items.map(x=>[x.speechText,x.outputFile]),[['요','yo.mp3'],['유','yu.mp3']]);
assert.doesNotMatch(workflow,/^\s{2}(push|pull_request|schedule):/m);
assert.match(workflow,/^\s{2}workflow_dispatch:/m);
assert.match(workflow,/mode:[\s\S]*dry-run[\s\S]*generate/);
assert.match(workflow,/batchId:[\s\S]*audio-batch-01/);
assert.match(workflow,/concurrency:[\s\S]*group:.*inputs\.batchId/);
assert.doesNotMatch(workflow,/git push|contents:\s*write/);
assert.match(workflow,/fetch-depth:\s*0/);
assert.match(workflow,/git fetch --no-tags origin main:refs\/remotes\/origin\/main/);
assert.match(workflow,/validate-repository-safety\.mjs --base-ref origin\/main --baseline-only/);
assert.match(workflow,/NIKIGO_BASE_REF:\s*origin\/main/);
assert.match(workflow,/Upload staged audio and reports[\s\S]*if:\s*always\(\)/);
assert.match(workflow,/retention-days:\s*7/);
assert.ok(workflow.indexOf('Run complete tests before any generation')<workflow.indexOf('Generate paid audio into staging'));
const dryStep=workflow.match(/- name: Create no-cost dry-run staging fixtures[\s\S]*?(?=\n\s+- name:)/)?.[0]||'';
assert.doesNotMatch(dryStep,/OPENAI_API_KEY|generate paid|api\.openai/i);
const paidStep=workflow.match(/- name: Generate paid audio into staging[\s\S]*?(?=\n\s+- name:)/)?.[0]||'';
assert.match(paidStep,/OPENAI_API_KEY/);assert.match(paidStep,/inputs\.mode == 'generate'/);
assert.doesNotMatch(workflow,/retry|max-attempts/);

const temp=fs.mkdtempSync(path.join(os.tmpdir(),'nikigo-audio-dry-run-'));
const denyNetwork=path.join(temp,'deny-network.cjs');
fs.writeFileSync(denyNetwork,"global.fetch=()=>{throw new Error('DRY_RUN_NETWORK_ACCESS_FORBIDDEN')};\n");
const secretVariableName=['OPENAI','API','KEY'].join('_');
const unusedSecretMarker=['MUST','NOT','BE','USED'].join('_');
const base=['--batch-id','audio-batch-01','--mode','dry-run','--expected-count','2','--staging-dir',temp,'--workflow-run-id','dry-test','--commit','test'];
let result=spawnSync(process.execPath,[path.join(root,'scripts/generate-lesson-audio.mjs'),...base],{cwd:root,encoding:'utf8',env:{...process.env,[secretVariableName]:unusedSecretMarker,NODE_OPTIONS:`--require=${denyNetwork}`}});
assert.equal(result.status,0,result.stderr);
result=spawnSync(process.execPath,[path.join(root,'scripts/validate-audio-staging.mjs'),...base],{cwd:root,encoding:'utf8'});
assert.equal(result.status,0,result.stderr);
const artifact=JSON.parse(fs.readFileSync(path.join(temp,'artifact-manifest.json'),'utf8'));
assert.equal(artifact.generatedCount,2);assert.equal(artifact.apiRequestCount,0);assert.equal(artifact.reviewStatus,'underReview');assert.equal(artifact.items.length,2);
assert.deepEqual(artifact.items.map(x=>x.codec),['fixture','fixture']);
assert.doesNotMatch(JSON.stringify(artifact),/MUST_NOT_BE_USED|OPENAI_API_KEY|Authorization/i);
const formal=JSON.parse(fs.readFileSync(path.join(root,'audio/lesson-00/manifest.json'),'utf8'));
assert.ok(formal.items.every(x=>x.reviewStatus==='pending'&&x.assetStatus==='missing'));
assert.doesNotMatch(fs.readFileSync(path.join(root,'audio-catalog.js'),'utf8'),/staging\//);
assert.doesNotMatch(fs.readFileSync(path.join(root,'sw.js'),'utf8'),/staging\//);

const broken=fs.mkdtempSync(path.join(os.tmpdir(),'nikigo-audio-broken-'));
fs.cpSync(temp,broken,{recursive:true});fs.unlinkSync(path.join(broken,'files/yu.mp3.fixture'));
result=spawnSync(process.execPath,[path.join(root,'scripts/validate-audio-staging.mjs'),'--batch-id','audio-batch-01','--mode','dry-run','--expected-count','2','--staging-dir',broken],{cwd:root,encoding:'utf8'});
assert.notEqual(result.status,0,'Missing staged file must fail technical validation.');
assert.ok(fs.existsSync(path.join(broken,'artifact-manifest.json')),'Failure must still preserve an artifact manifest.');
const mismatch=fs.mkdtempSync(path.join(os.tmpdir(),'nikigo-audio-mismatch-'));
result=spawnSync(process.execPath,[path.join(root,'scripts/generate-lesson-audio.mjs'),'--batch-id','audio-batch-01','--mode','dry-run','--expected-count','3','--staging-dir',mismatch],{cwd:root,encoding:'utf8',env:{...process.env,[secretVariableName]:unusedSecretMarker}});
assert.notEqual(result.status,0,'Count mismatch must fail before generation.');
assert.equal(fs.existsSync(path.join(mismatch,'files/yo.mp3.fixture')),false);
console.log('Validated Batch 1 isolation, exact two-item allowlist, no-cost dry-run, failure-preserved reports, and workflow trigger/secret safeguards.');
